package org.wso2.carbon.device.mgt.common.general;

import java.util.Map;

public class OneTimeTokenDetails extends TenantDetail {

    String password;
    String token;
    long createdDate;
    long updatedDate;
    boolean isExpired;
    String metaInfo;
    Map<String, String> replaceValue;
    String emailType;

    public long getCreatedDate() {
        return createdDate;
    }

    public void setCreatedDate(long createdDate) {
        this.createdDate = createdDate;
    }

    public long getUpdatedDate() {
        return updatedDate;
    }

    public void setUpdatedDate(long updatedDate) {
        this.updatedDate = updatedDate;
    }

    public boolean isExpired() {
        return isExpired;
    }

    public void setExpired(boolean expired) {
        isExpired = expired;
    }

    public String getMetaInfo() {
        return metaInfo;
    }

    public void setMetaInfo(String metaInfo) {
        this.metaInfo = metaInfo;
    }

    public Map<String, String> getReplaceValue() {
        return replaceValue;
    }

    public void setReplaceValue(Map<String, String> replaceValue) {
        this.replaceValue = replaceValue;
    }

    public String getEmailType() {
        return emailType;
    }

    public void setEmailType(String emailType) {
        this.emailType = emailType;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }
}
